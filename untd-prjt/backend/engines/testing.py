from models.schemas import TestingResult, TestCase
from engines.model_router import get_task_model

MAX_CODE_CHARS = 30000

def _truncate_code(code: str) -> str:
    """Truncate large project code to fit within LLM context limits."""
    if len(code) <= MAX_CODE_CHARS:
        return code
    truncated = code[:MAX_CODE_CHARS]
    last_boundary = truncated.rfind("\n--- ")
    if last_boundary > MAX_CODE_CHARS * 0.5:
        truncated = truncated[:last_boundary]
    return truncated + "\n\n# ... (remaining project files truncated for analysis) ..."


def _fallback_llm_testing(code: str, language: str) -> TestingResult:
    from openai import OpenAI
    
    api_key = os.environ.get("NVIDIA_API_KEY")
    if not api_key:
        return TestingResult(
            total_tests=0,
            coverage_summary=f"NVIDIA_API_KEY is required to natively mock tests for '{language}'.",
        )
    
    try:
        client = OpenAI(base_url="https://integrate.api.nvidia.com/v1", api_key=api_key)
        analysis_code = _truncate_code(code)
        is_project = "--- " in code and code.count("--- ") > 1
        context_hint = (
            "This is a MULTI-FILE PROJECT. Focus on the most critical functions and their interactions across modules."
            if is_project
            else "This is a single code snippet."
        )
        prompt = f"""You are a senior QA Testing Engineer for {language}.
{context_hint}
Analyze the provided code and generate exactly 15 logical simulated test cases testing the core functionality.
Categories to include: Unit, Integration, BVA, ECP, Negative, Regression.

You must SIMULATE what the actual returned values or errors would be. 
Return ONLY valid JSON matching this exact structure:
{{
  "test_cases": [
    {{"name": "test_example", "category": "Unit", "passed": true, "input_data": "sample", "expected": "output", "actual": "output", "message": "pass"}}
  ]
}}
IMPORTANT: Output raw JSON only. Do not wrap in markdown blocks. Do not add any text before or after the JSON.

Code:
{analysis_code}"""
        comp = client.chat.completions.create(
            model=get_task_model("legacy_testing", "ibm/granite-34b-code-instruct"),
            messages=[{"role": "user", "content": prompt}],
            temperature=0.1,
            max_tokens=8192,
        )
        res_text = comp.choices[0].message.content.strip()
        
        # Clean markdown if present
        if res_text.startswith("```"):
            lines = res_text.split("\n")
            if lines[0].startswith("```"): lines = lines[1:]
            if lines and lines[-1].startswith("```"): lines = lines[:-1]
            res_text = "\n".join(lines).strip()
        
        if res_text.lower().startswith("json"):
            res_text = res_text[4:].strip()

        # Final check for JSON-like start/end
        start_idx = res_text.find('{')
        end_idx = res_text.rfind('}')
        if start_idx != -1 and end_idx != -1:
            res_text = res_text[start_idx:end_idx+1]
            
        try:
            data = json.loads(res_text)
        except json.JSONDecodeError as jde:
            print(f"JSON Decode Error in Testing Engine: {str(jde)}")
            # Attempt a simpler parse or fallback
            raise jde

        tests = []
        passed_count = 0
        for tc in data.get("test_cases", []):
            passed_status = tc.get("passed", False)
            if passed_status: 
                passed_count += 1
            tests.append(TestCase(
                name=str(tc.get("name", "test")),
                category=str(tc.get("category", "Unit")),
                passed=bool(passed_status),
                input_data=str(tc.get("input_data", "")),
                expected=str(tc.get("expected", "")),
                actual=str(tc.get("actual", "")),
                message=str(tc.get("message", ""))
            ))
            
        if not tests:
            raise ValueError("No test cases generated by LLM.")

        return TestingResult(
            total_tests=len(tests),
            passed=passed_count,
            failed=len(tests) - passed_count,
            test_cases=tests,
            coverage_summary=f"Automated QA Framework generated {len(tests)} test cases across multiple categories for {language}."
        )
    except Exception as e:
        print(f"Testing Engine critical failure: {str(e)}")
        return TestingResult(
            total_tests=1,
            passed=0,
            failed=1,
            test_cases=[TestCase(
                name="Automated Suite Generation Error", 
                category="System", 
                passed=False, 
                input_data="N/A", 
                expected="Valid JSON Test Suite", 
                actual=f"Error: {str(e)[:100]}...", 
                message="The AI engine encountered an error while synthesizing the test cases. Please try re-running analysis."
            )],
            coverage_summary=f"QA Simulator error: {str(e)}"
        )

def run_tests(code: str, language: str = "python") -> TestingResult:
    """
    Generate and execute advanced automated tests against the submitted code using the AI Framework.
    """
    return _fallback_llm_testing(code, language)
